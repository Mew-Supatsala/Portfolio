function openModal(key) {
  const data = workData[key];
  if (!data) return;

  document.getElementById("modalTitle").innerText = data.title;
  document.getElementById("modalText").innerHTML = data.text;

  const mainImg = document.getElementById("modalMainImage");
  const thumbs = document.getElementById("modalThumbs");

  thumbs.innerHTML = "";
  mainImg.src = data.images[0];

  data.images.forEach((img, index) => {
    const thumb = document.createElement("img");
    thumb.src = img;

    if (index === 0) thumb.classList.add("active");

    thumb.onclick = (e) => {
      e.stopPropagation();
      mainImg.src = img;
      document.querySelectorAll(".thumbs img").forEach(t => t.classList.remove("active"));
      thumb.classList.add("active");
    };

    thumbs.appendChild(thumb);
  });

  document.getElementById("workModal").classList.add("show");
}


function closeModal() {
  document.getElementById("workModal").classList.remove("show");
}


